package com.banana.vo;

public class ChatVO {
	String cid, sell_mid, buy_mid, pid, chat_content, cdate, chk_read;
	String nickname, score, maddr, psfile;
	
	
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getSell_mid() {
		return sell_mid;
	}
	public void setSell_mid(String sell_mid) {
		this.sell_mid = sell_mid;
	}
	public String getBuy_mid() {
		return buy_mid;
	}
	public void setBuy_mid(String buy_mid) {
		this.buy_mid = buy_mid;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getChat_content() {
		return chat_content;
	}
	public void setChat_content(String chat_content) {
		this.chat_content = chat_content;
	}
	public String getCdate() {
		return cdate;
	}
	public void setCdate(String cdate) {
		this.cdate = cdate;
	}
	public String getChk_read() {
		return chk_read;
	}
	public void setChk_read(String chk_read) {
		this.chk_read = chk_read;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getMaddr() {
		return maddr;
	}
	public void setMaddr(String maddr) {
		this.maddr = maddr;
	}
	public String getPsfile() {
		return psfile;
	}
	public void setPsfile(String psfile) {
		this.psfile = psfile;
	}
	
	
}
